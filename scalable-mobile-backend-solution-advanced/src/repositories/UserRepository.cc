#include "UserRepository.h"
#include "../database/DBManager.h"

UserRepository::UserRepository(drogon::orm::DbClientPtr client) {
    if (client) {
        dbClient = client;
    } else {
        dbClient = DBManager::getClient();
    }
}

drogon::Task<std::optional<User>> UserRepository::findById(const UUID& id) {
    try {
        auto result = co_await dbClient->execSqlCoro("SELECT * FROM users WHERE id = $1", id);
        if (result.empty()) {
            co_return std::nullopt;
        }
        co_return User::fromDrogonModel(result[0]);
    } catch (const drogon::orm::DrogonDbException& e) {
        LOG_ERROR("Database error finding user by ID {}: {}", id, e.what());
        throw Common::ApiException(500, "Database error: " + std::string(e.what()));
    }
}

